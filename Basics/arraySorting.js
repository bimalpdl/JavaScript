const fruits = ["Orange", "Banana", "Kiwi", "Apple", "Mango"];
console.log(`Original array: ${fruits}`);

// The reverse() method reverses the elements in an array:
console.log(`Reversed array: ${fruits.reverse()}`)

// The sort() method sorts an array alphabetically:
console.log(`Sorted array: ${fruits.sort()}`);

// By combining sort() and reverse(), you can sort an array in descending order:
console.log(`Reverse sorted array: ${fruits.sort().reverse()}`);

//  toSorted() method as a safe way to sort an array without altering the original array. The difference between toSorted() and sort() is that the first method creates a new array, keeping the original array unchanged, while the last method alters the original array.

